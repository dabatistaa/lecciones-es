    <h2>Sara y los Hijos de la Libre</h2> <!--Leccion Titulo-->
    <h4>Sara significa “Princesa”</h4> <!--Leccion Subtitulo-->
    <p> <!--Nota Introduccion -->
      “De manera, hermanos, que no somos hijos de la esclava, sino de la libre” Gálatas 4:22.
    </p>
    <p> <!--Nota Introduccion -->
      “El nombre de Sarai se cambió por el de Sara, ‘princesa’; pues, dijo la divina voz, ‘vendrá a ser madre de naciones; reyes de pueblos serán de ella.’
    </p>
    <p> <!--Nota Introduccion -->
       “La unión de Abraham con Agar resultó perjudicial, no solamente para su propia casa, sino también para las generaciones futuras” (Patriarcas y Profetas, págs. 117, 125).
    </p>
