    <!-- <h3>Subtitulo</h3> Subtitulo  -->
    <h4>1. ¿Qué relación tienen Sara y Agar con Abraham? ¿Por qué Isaac era considerado el hijo de la promesa y el único hijo de Abraham, cuando Ismael fue su primer hijo?</h4> <!--Pregunta -->
    <p><strong>Génesis 12:16, 18, 19</strong></p> <!--Texto 1-->
    <p>
      “Y por causa de ella [Sara], Faraón trató bien a Abram, y le regaló ovejas y vacas, asnos, siervos y criadas, asnas y camellos.” “Entonces Faraón llamó a Abram y le dijo: ‘¿Qué has hecho conmigo? ¿Por qué no me declaraste que era tu esposa?’ ‘¿Por qué dijiste: ‘Es mi hermana’ y me pusiste en ocasión de tomarla para mí por esposa? Ahora ahí está tu esposa. Tómala y vete”
    </p>
    <p><strong>Génesis 20:12</strong></p> <!--Texto 2-->
    <p>
      “Y a la verdad, también es mi hermana, hija de mi padre, pero no de mi madre. Y la tomé por esposa”
    </p>
    <p><strong>Gálatas 4:22, 23</strong></p> <!--Texto 3-->
    <p>
      “Porque escrito está que Abrahán tuvo dos hijos; uno de la esclava, el otro de la libre. El de la esclava nació según la ley natural de la carne. El de la libre nació por la promesa”
    </p>
    <p><strong>Nota</strong></p> <!--Nota -->
    <p>
      “Sara era una mujer ‘hermosa en gran manera’ (Génesis 12:14), y Abraham no dudó que los egipcios de piel oscura codiciarían a la hermosa extranjera, y que para conseguirla, no tendrían escrúpulos en matar a su esposo. Razonó que no mentía al presentar a Sara como su hermana; pues ella era hija de su padre, aunque no de su madre. Pero este ocultamiento de la verdadera relación que existía entre ellos era un engaño. Ningún desvío de la estricta integridad puede merecer la aprobación de Dios” (Hijas de Dios, pág. 24).
    </p>
    <p>
      “Agar era orgullosa y jactanciosa, y se comportó con arrogancia frente a Sara. Se vanagloriaba de que sería la madre de la gran nación que Dios le había prometido a Abrahán. Y éste se vio obligado a escuchar las quejas de Sara con respecto a la conducta de la sierva, y sus acusaciones de que el patriarca se había equivocado en este asunto. Éste se afligió y le dijo a Sara que Agar era su sierva, y que estaba a sus órdenes, pero no quiso despedirla porque iba a ser la madre del hijo mediante el cual se cumpliría la promesa, según él creía. Dijo a Sara que no habría tomado a Agar por esposa si ella no se lo hubiera pedido de manera especial.
    </p>
    <p>
      “También se vio obligado a escuchar las quejas de la sierva por los abusos de Sara. Estaba perplejo. Si trataba de rectificar los errores cometidos contra Agar, aumentaría los celos y la infelicidad de Sara, su primera y muy amada esposa” (Historia de la Redención, págs. 79, 80).
    </p>
