    <h4>2. Después de haber mentido a Dios, ¿qué lección tuvo que aprender Sara para llegar a ser madre espiritual de Israel y de los salvos?</h4> <!--Pregunta -->
    <p><strong>Isaías 57:11</strong></p> <!--Texto 1-->
    <p>
      “¿De quién te asustaste y temiste, que mentiste, y no te acordaste de mí, ni te vino al pensamiento? ¿No es porque yo guardé silencio desde tiempos antiguos, por lo que nunca me has temido?”
    </p>
    <p><strong>Génesis 21:6, 7</strong></p> <!--Texto 2-->
    <p>
      “Entonces dijo Sara: ‘Dios me hizo reír, y el que lo oiga, se reirá conmigo’. Y añadió: ‘¿Quién hubiera dicho a Abrahán que Sara habría de amamantar a hijos? Pues le he dado un hijo en su vejez’”
    </p>
    <p><strong>Romanos 4:20-22</strong></p> <!--Texto 3-->
    <p>
      “Tampoco dudó, ni desconfió de la promesa de Dios, antes se fortaleció por la fe, y dio gloria a Dios, plenamente convencido que Dios tenía poder para cumplir lo que había prometido. Por eso, su fe le fue contada por justicia”
    </p>
    <p><strong>Nota</strong></p> <!--Nota -->
    <p>
      “Tanto Abraham como Sara desconfiaron del poder de Dios, y este error fue la causa del matrimonio con Agar. Dios había llamado a Abraham para que fuera el padre de los fieles, y su vida había de servir como ejemplo de fe para las generaciones futuras. Pero su fe no había sido perfecta. Había manifestado desconfianza en Dios al ocultar el hecho de que Sara era su esposa, y también al casarse con Agar” (Patriarcas y Profetas, pág. 127).
    </p>
